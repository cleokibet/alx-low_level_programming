#include <stdio.h>
/**
 * add - adds two integers
 * @a: integer 1
 * @b: integer 2
 * Return: sum of the integers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two integers
 * @a: integer 1
 * @b: integer 2
 * Return: difference of two integers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: integer 1
 * @b: integer 2
 * Return: product of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two integers
 * @a: integer 1
 * @b: integer 2
 * Return: division of two integers
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - determines modulus of two integers
 * @a: integer 1
 * @b: integer 2
 * Return: modulus of two integers
 */
int mod(int a, int b)
{
	return (a % b);
}
